<?php
// Exemplo de Expressões Aritméticas em PHP

// Definindo dois números
$numero1 = 10;
$numero2 = 5;

// Adição
$soma = $numero1 + $numero2;
echo "A soma é: $soma<br>";

// Subtração
$subtracao = $numero1 - $numero2;
echo "A subtração é: $subtracao<br>";

// Multiplicação
$multiplicacao = $numero1 * $numero2;
echo "A multiplicação é: $multiplicacao<br>";

// Divisão
$divisao = $numero1 / $numero2;
echo "A divisão é: $divisao<br>";

// Módulo (Resto da divisão)
$resto = $numero1 % $numero2;
echo "O resto da divisão é: $resto<br>";

// Incremento
$incremento = $numero1;
$incremento++;
echo "O incremento é: $incremento<br>";

// Decremento
$decremento = $numero1;
$decremento--;
echo "O decremento é: $decremento<br>";

// Usando parênteses para controlar a ordem de execução
$resultado = (2 + 3) * 4;
echo "O resultado é: $resultado<br>";
?>
